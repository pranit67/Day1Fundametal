package com.bridgelabz.fundamental;

public  class PrimetiveDataType {
	static Long abc;
	static boolean tf;
	static byte gh;
	static float hgg;
	static int gdgdg;
	static String hshddhString;
	public static void main(String[] args) {
		System.out.println("long default value="+ abc);
		System.out.println("boolean default value="+ tf);
		System.out.println("byte default value="+ gh);
		System.out.println("float default value="+ hgg);
		System.out.println("int default value="+ gdgdg);
		System.out.println("String default value="+ hshddhString);
	}
}
